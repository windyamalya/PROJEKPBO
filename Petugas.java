public class Petugas {
    protected int id;
    protected String nama;
    protected int no_hp;



    public void setId(int id){
        this.id=id;
    }

    public int getId() {
        return id;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    public String getNama() {
        return nama;
    }

    public void setNo_hp(int no_hp) {
        this.no_hp = no_hp;
    }

    public int getNo_hp() {
        return no_hp;
    }
}
